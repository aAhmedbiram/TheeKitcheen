function applyTranslations(lang) {
    const dict = translations[lang] || translations.ar;
    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.getAttribute("data-i18n");
        if (dict[key]) {
            const text = dict[key].replace(/\\n/g, "\n");
            el.textContent = text;
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
        const key = el.getAttribute("data-i18n-placeholder");
        if (dict[key]) {
            el.placeholder = dict[key];
        }
    });

    document.body.setAttribute("data-lang", lang);
    document.documentElement.lang = lang === "ar" ? "ar" : "en";
    document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

    // Update admin products table language (names) if present
    if (document.getElementById("admin-products-body")) {
        loadAdminProducts().catch(() => {});
        setAdminSaveMode(document.getElementById("admin-save-product")?.dataset?.mode || "create");
    }
}

function setupLanguageToggle() {
    const toggle = document.getElementById("lang-toggle");
    if (!toggle) return;

    let currentLang = localStorage.getItem("matbakhna_lang") || "ar";
    applyTranslations(currentLang);

    toggle.addEventListener("click", () => {
        currentLang = currentLang === "ar" ? "en" : "ar";
        localStorage.setItem("matbakhna_lang", currentLang);
        applyTranslations(currentLang);
    });
}

