async function apiFetch(path, options) {
    const res = await fetch(path, {
        headers: { "Content-Type": "application/json" },
        ...options
    });
    const data = await res.json().catch(() => ({}));
    if (!res.ok || data.ok === false) {
        const msg = data.error || `Request failed (${res.status})`;
        throw new Error(msg);
    }
    return data;
}

